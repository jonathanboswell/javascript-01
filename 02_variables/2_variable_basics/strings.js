//Declare a variable
var name;

//Assign a value
name = "Your name";

//Access the variable
console.log(name);

//Assign a new value
name = "Your friend's name";

//Access the variable
console.log(name);

//Assign by accessing the variable and adding or "concatenating" an additional string
name = name + " is my friend";

//Assign by using an assignment operator
name += " and a cool person.";

//Access the variable
console.log(name);